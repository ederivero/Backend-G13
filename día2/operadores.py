a = 5
b = 10

# Operadores aritméticos
print(a + b) # Suma
print(a - b) # Resta
print(a * b) # Multiplicación
print(a / b) # División
print(a // b) # División entera
print(a % b) # Módulo
print(a ** b) # Potencia

# Operadores de comparación
print(a == b) # Igualdad
print(a != b) # Desigualdad
print(a > b) # Mayor que
print(a < b) # Menor que
print(a >= b) # Mayor o igual que
print(a <= b) # Menor o igual que

# Operadores lógicos
print(a > b and a != b) # AND
print(a > b or a != b) # OR
print(not a > b) # NOT
